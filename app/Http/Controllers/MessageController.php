     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->has('langs')){
            $message = Message::first();
            $vars = ["id","location","created_at","updated_at"];
            $data = $message->getTableColumns();
            $langs = array_diff($data, $vars);
            return response()->json(["languages" => $langs], 404);
        }
        if(request('lang') == null){
            return response()->json(["error" => 'Message not available'], 404);
        }
        switch(request('lang')){
            case 'en':
                $final_message = Message::select('en', 'id')->get();
            break;
            default:
                return response()->json(["error" => 'Language not available'], 404);
        }
        return response()->json(["message" => $final_message], 200);
    }

